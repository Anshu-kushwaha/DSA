package Strings;

public class StringToCharArray {
    public static void main(String[] args) {
        String s = "Raghav";
        char [] arr = s.toCharArray();
        for (char ch : arr) {
            System.out.print(ch);
        }

        // iss tarike ko hum CamleCase kehte hai
        // iss way ko PascalCase kehte hai
    }
}









